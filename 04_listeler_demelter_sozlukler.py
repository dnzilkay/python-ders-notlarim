# Liste tanımlama
meyveler = ["Elma", "Armut", "Muz", "Çilek"]

# Listeye eleman ekleme
meyveler.append("Portakal")

# Belirli bir elemanı değiştirme
meyveler[1] = "Karpuz"

# Elemanları yazdırma
print(meyveler)


# Demet tanımlama
renkler = ("Kırmızı", "Mavi", "Yeşil")

# Elemanlara erişim
print(renkler[0])  # Kırmızı


# Sözlük tanımlama
kisiler = {"Ahmet": 25, "Mehmet": 30, "Zeynep": 22}

# Değere erişim
